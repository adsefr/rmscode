package com.rms.common.xml.dtd.bean;

public class DTDEntityBean {

	public String name;

	public String value;

	public String ndata;

	public String system;

	/**
	 * @return name
	 */
	public String getName() {

		return name;
	}

	/**
	 * @param name
	 *            セットする name
	 */
	public void setName(String name) {

		this.name = name;
	}

	/**
	 * @return value
	 */
	public String getValue() {

		return value;
	}

	/**
	 * @param value
	 *            セットする value
	 */
	public void setValue(String value) {

		this.value = value;
	}

	/**
	 * @return ndata
	 */
	public String getNdata() {

		return ndata;
	}

	/**
	 * @param ndata
	 *            セットする ndata
	 */
	public void setNdata(String ndata) {

		this.ndata = ndata;
	}

	/**
	 * @return system
	 */
	public String getSystem() {

		return system;
	}

	/**
	 * @param system
	 *            セットする system
	 */
	public void setSystem(String system) {

		this.system = system;
	}

}
