package com.rms.common.xml.dtd.bean;

public class DTDNotationBean {

	private String name;

	private String system;

	/**
	 * @return name
	 */
	public String getName() {

		return name;
	}

	/**
	 * @param name
	 *            セットする name
	 */
	public void setName(String name) {

		this.name = name;
	}

	/**
	 * @return system
	 */
	public String getSystem() {

		return system;
	}

	/**
	 * @param system
	 *            セットする system
	 */
	public void setSystem(String system) {

		this.system = system;
	}
}
