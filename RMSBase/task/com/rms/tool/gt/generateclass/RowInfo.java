package com.rms.tool.gt.generateclass;

public class RowInfo {

	private String seqNo;

	private String javaProcessName;

	private String vbProcessName;

	private String logicName;

	private String processDetail;

	private String packageName;

	private String className;

	private String returnType;

	private String methodName;

	private String parameterCount;

	private String methodParameterList;

	private String targetType;

	private String targetTypeOld;

	private String userId;

	private String filePath;

	public RowInfo() {

	}

	public String getSeqNo() {

		return seqNo;
	}

	public void setSeqNo(String seqNo) {

		this.seqNo = seqNo;
	}

	public String getJavaProcessName() {

		return javaProcessName;
	}

	public void setJavaProcessName(String javaProcessName) {

		this.javaProcessName = javaProcessName;
	}

	public String getVbProcessName() {

		return vbProcessName;
	}

	public void setVbProcessName(String vbProcessName) {

		this.vbProcessName = vbProcessName;
	}

	public String getLogicName() {

		return logicName;
	}

	public void setLogicName(String logicName) {

		this.logicName = logicName;
	}

	public String getProcessDetail() {

		return processDetail;
	}

	public void setProcessDetail(String processDetail) {

		this.processDetail = processDetail;
	}

	public String getPackageName() {

		return packageName;
	}

	public void setPackageName(String packageName) {

		this.packageName = packageName;
	}

	public String getClassName() {

		return className;
	}

	public void setClassName(String className) {

		this.className = className;
	}

	public String getReturnType() {

		return returnType;
	}

	public void setReturnType(String returnType) {

		this.returnType = returnType;
	}

	public String getMethodName() {

		return methodName;
	}

	public void setMethodName(String methodName) {

		this.methodName = methodName;
	}

	public String getParameterCount() {

		return parameterCount;
	}

	public void setParameterCount(String parameterCount) {

		this.parameterCount = parameterCount;
	}

	public String getMethodParameterList() {

		return methodParameterList;
	}

	public void setMethodParameterList(String methodParameterList) {

		this.methodParameterList = methodParameterList;
	}

	public String getTargetType() {

		return targetType;
	}

	public void setTargetType(String targetType) {

		this.targetType = targetType;
	}

	public String getTargetTypeOld() {

		return targetTypeOld;
	}

	public void setTargetTypeOld(String targetTypeOld) {

		this.targetTypeOld = targetTypeOld;
	}

	public String getUserId() {

		return userId;
	}

	public void setUserId(String userId) {

		this.userId = userId;
	}

	public String getFilePath() {

		return filePath;
	}

	public void setFilePath(String filePath) {

		this.filePath = filePath;
	}

}
